/**
 *Prints the letter J on multiple lines.
 *
 *@author Justin Sawyer
 *@version 01-24-16
 */
public class JLetter
{
   /**
    * Prints java into letter "J".
    *
    * @param args Command line arguments (not used).
    */
   public static void main(String[] args)
   {
      //prints out a "J" in ten lines
      System.out.println("JAVAJAVAJAVA\nJAVAJAVAJAVA\n      JAVA"
         + "\n      JAVA\n      JAVA\n      JAVA\nJ     JAVA\nJA    JAVA"
         + "\n JAVAJAVA\n  JAVAJA");
   }
}